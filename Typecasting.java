public class Typecasting {
    public static void main(String args[]){
        double d1=99.999;
        int d2 =(int)d1;
        System.out.println(d1);
        System.out.println(d2);

        int a=66; 
        char ch = (char)a;
        System.out.println(ch);
    }

}
//Converting one data type into another data type.
//1.Implicit typecasting:Smaller datatype automatically converts into bigger datatype.
//2.Explicit typecasting:Bigger datatype converted into smaller datatype manually.